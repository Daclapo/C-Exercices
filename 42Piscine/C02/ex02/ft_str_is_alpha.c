#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z'))
			i++;
		else
			return (0);
	}
	return (1);
}

int main() {
    // test your ft_str_is_alpha function here
    const char *str = "Hello, World!";
    int result = ft_str_is_alpha(str);
    printf("Result: %d\n", result);
    return 0;
}